// Datatypes In Java
public class Main2 {
    public static void main(String[] args) {
        
        // INTEGERS
        System.out.println("=================INTEGERS==============");

        byte b1 = 75;
        System.out.println("Byte value = "+"b1="+b1);

        short s1 = 100;
        System.out.println("Short value = "+"S1 = " + s1);

        int i1 = 1234;
        System.out.println("Int value = "+"i1 = "+i1);

        long l1 = 123456789;
        System.out.println("Long value = "+"l1 = "+l1);

        // FLOATING POINT NUMBERS
        System.out.println("=================FLOATING POINT NUMBERS==============");

        float f1 = 12.345f;
        System.out.println("Float value = "+"f1 = "+f1);

        double d1 = 123.612;
        System.out.println("Double Value = "+"D1 = "+d1);

        // CHARACTERS
        System.out.println("=================CHARACTERS==============");
        
        char c1,c2;
        c1 = 88;
        c2 = 'Y';
        System.out.println("Character values = C1 = "+c1+"and C2 =" +c2);

        // BOOLEAN
        System.out.println("=================BOOLEAN==============");
        boolean b;
        b = false;
        System.out.println("Boolean value B1 = "+b);
        b = true;
        System.out.println("Boolean value B2 = "+b);

    }
}
